    /**
     * @var string
     * @access private
     *
     * @ORM\Column(name="name", type="string", length=320, nullable=false, unique=true)
     * 
     * @Expose
     * 
     */
    private $name;

    /**
     * @var string
     * @access private
     *
     * @ORM\Column(name="html_file", type="string", length=320, nullable=true, unique=false)
     * 
     * @Expose
     * 
     */
    private $htmlFile;

    /**
     * @var string
     * @access private
     *
     * @ORM\Column(name="css_file", type="string", length=320, nullable=true, unique=false)
     * 
     * @Expose
     * 
     */
    private $cssFile;

    /**
     * @var \DateTime
     * @access private
     *
     * @ORM\Column(name="created_at", type="datetime", nullable=false, unique=false)
     * 
     * @Expose
     * 
     */
